namespace spaghettis {

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

enum class DragFlag { None, Move, Resize, Pin };

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

class EditView;

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

class Dragable {

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -
    
public:
    explicit Dragable (PatchView*);
    
    virtual ~Dragable();

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

public:
    PatchView* getView() const
    {
        return view_;
    }

    EditView* getEditView() const;
    
// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

public:
    bool forwardMouseDown (const juce::MouseEvent&, PainterPolicy*);
    void forwardMouseDrag (const juce::MouseEvent&, PainterPolicy*, DragFlag);
    void forwardMouseUp (const juce::MouseEvent&, PainterPolicy*);
    
private:
    PatchView* view_;
    
private:
    bool isForwarding_;
    bool isDraggingView_;
    
// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------

};

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------

}

