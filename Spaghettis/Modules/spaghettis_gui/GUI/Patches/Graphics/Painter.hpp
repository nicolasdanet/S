static int pinWidth  (float f) { return static_cast<int> (std::round (8 * f)); }
static int pinGripX  (float f) { return (f >= 1.0f) ? 1 : 0; }
static int pinGripY  (float f) { return (f >= 1.0f) ? 3 : 0; }

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

