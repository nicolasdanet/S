PainterPolicy::PainterPolicy (juce::Component& owner, const core::Object& object) :
    owner_ (owner),
    object_ (object),
    x_ (fetchAttribute<int> (Tags::X)),
    y_ (fetchAttribute<int> (Tags::Y))
{
    auto f = [c = dynamic_cast<ObjectComponent*> (&owner_)] ()
    {
        jassert (c); DBG ("?");
    };
    
    x_.attach (f);
    y_.attach (f);
}
    
