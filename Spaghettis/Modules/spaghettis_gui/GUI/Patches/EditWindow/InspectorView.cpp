InspectorView::InspectorView (Sync&& s, int w) :
    ParameterView (s.getData(), Spaghettis()->getLookAndFeel().getConsoleFont(), w),
    sync_ (std::move (s))
{
    sync_.bind (this);
}

InspectorView::~InspectorView()
{
    sync_.unbind (this);
}

