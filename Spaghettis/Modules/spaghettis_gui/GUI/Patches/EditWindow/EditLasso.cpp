void EditLasso::mouseDrag (const juce::MouseEvent& e)
{
    const juce::Point<int> a (e.getMouseDownPosition());
    const juce::Point<int> b (a + e.getOffsetFromDragStart());
    const juce::Rectangle<int> r (a, b);
    
    update (r);
}

