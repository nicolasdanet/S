namespace spaghettis {

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

class RunLayoutElement {

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

public:
    explicit RunLayoutElement (ObjectComponent* object, const juce::Rectangle<int>& bounds) :
        object_ (object),
        bounds_ (bounds)
    {
        static_assert (std::is_trivially_copyable_v<RunLayoutElement> == true);
        static_assert (std::is_nothrow_move_constructible_v<RunLayoutElement> == true);
        static_assert (std::is_nothrow_move_assignable_v<RunLayoutElement> == true);
    }

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

public:
    ObjectComponent* getObject() const
    {
        return object_;
    }
    
    juce::Rectangle<int> getBounds() const
    {
        return bounds_;
    }

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

public:
    explicit operator ObjectComponent*() const
    {
        return getObject();
    }
    
// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

public:
    void setBounds (const juce::Rectangle<int>& bounds)
    {
        bounds_ = bounds;
    }
    
private:
    ObjectComponent* object_;
    juce::Rectangle<int> bounds_;
};

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------

}

