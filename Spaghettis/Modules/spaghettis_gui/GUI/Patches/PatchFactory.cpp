/* Note that at this point the owner component is not fully constructed. */

void PatchFactory::setToolbarButton (IconsButton* button)
{
    switch (button->getItemId()) {
    //
    case Icons::edit    : button->setTooltip (NEEDS_TRANS ("Edit mode")); break;
    default             : break;
    //
    }
    
    switch (button->getItemId()) {
    //
    case Icons::edit    : button->onClick = []() { DBG ("!"); }; break;
    default             : break;
    //
    }
}

