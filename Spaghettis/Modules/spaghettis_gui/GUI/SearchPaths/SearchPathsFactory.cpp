void SearchPathsFactory::setCallback (int itemId, juce::ToolbarButton* button)
{
    switch (itemId) {
    //
    case Icons::add     : button->onClick = [this]() { owner_->addPaths(); };               break;
    case Icons::remove  : button->onClick = [this]() { owner_->removeSelectedPaths(); };    break;
    default             : break;
    //
    }
}
    
