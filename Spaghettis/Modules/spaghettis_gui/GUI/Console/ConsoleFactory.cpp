void ConsoleFactory::setCallback (int itemId, juce::ToolbarButton* button)
{
    switch (itemId) {
    //
    case Icons::clear   : button->onClick = [this]() { owner_->clear(); }; break;
    default             : break;
    //
    }
}
    
