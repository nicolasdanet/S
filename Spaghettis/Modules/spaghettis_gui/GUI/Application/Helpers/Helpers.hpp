namespace spaghettis {

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

struct Helpers {

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

static juce::String firstLetterCapitalized (juce::String s)
{
    return s.substring (0, 1).toUpperCase() + s.substring (1);
}

static juce::String upToWhitespace (juce::String s)
{
    return s.upToFirstOccurrenceOf (" ", false, false);
}

static juce::String withFixedNumberOfDigits (juce::String s, int n)
{
    juce::String t;

    for (auto c : s) {
        if (n <= 0) { break; }
        else {
            t += c; if (juce::CharacterFunctions::isDigit (c)) { --n; }
        }
    }
    
    return t;
}

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

static juce::SparseSet<int> getSparseSetFor (int i)
{
    juce::SparseSet<int> set; set.addRange ({i, i + 1}); return set;
}
 
// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------

};

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------

}

