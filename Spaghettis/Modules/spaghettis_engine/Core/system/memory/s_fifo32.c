#include "../../m_headers.h"

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

#define FIFO32_SIZE     8192    /* Must be a power of two. */
#define FIFO32_BYTES    4

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

t_fifo32 *fifo32_new (void)
{
    return fifo_shared_new (FIFO32_SIZE, FIFO32_BYTES);
}

void fifo32_free (t_fifo32 *x)
{
    fifo_shared_free (x);
}

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

int fifo32_getAvailableRead (t_fifo32 *x)
{
    return fifo_shared_getAvailableRead (x);
}

int fifo32_getAvailableWrite (t_fifo32 *x)
{
    return fifo_shared_getAvailableWrite (x, FIFO32_SIZE);
}

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

int fifo32_write (t_fifo32 *x, const void *data, int n)
{
    return fifo_shared_write (x, FIFO32_SIZE, FIFO32_BYTES, data, n);
}

int fifo32_read (t_fifo32 *x, void *data, int n)
{
    return fifo_shared_read (x, FIFO32_SIZE, FIFO32_BYTES, data, n);
}

