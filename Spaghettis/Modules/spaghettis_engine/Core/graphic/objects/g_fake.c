#define FAKE_FONT   16

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

int gui_getInletWidth()
{
    return FAKE_FONT * 3;
}

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------

t_rectangle gui_getBoundingBox (t_object *x)
{
    t_rectangle t;
    
    int n = buffer_getSize (object_getBuffer (x));
    int a = object_getX (x);
    int b = object_getY (x);
    int c = a;
    int d = b;
    
    c += FAKE_FONT * n;
    d += FAKE_FONT;
    
    rectangle_set (&t, a, b, c, d);

    return t;
}

