#ifndef M_BINDLIST_H_
#define M_BINDLIST_H_

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

typedef struct _bindelement {
    t_pd                    *e_what;        /* MUST be the first. */
    struct _bindelement     *e_next;
    } t_bindelement;

typedef struct _bindlist {
    t_pd                    b_pd;           /* MUST be the first. */
    int                     b_used;
    t_bindelement           *b_cached;
    t_bindelement           *b_list;
    } t_bindlist;

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

void    bindlist_add                (t_symbol *s, t_pd *x);
t_error bindlist_remove             (t_symbol *s, t_pd *x);

int     bindlist_hasThing           (t_symbol *s);

t_pd    *bindlist_getThingByClass   (t_symbol *s, t_class *c);

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
#endif // M_BINDLIST_H_
