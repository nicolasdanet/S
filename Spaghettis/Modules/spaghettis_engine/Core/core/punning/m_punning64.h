#ifndef M_PUNNING_64_H_
#define M_PUNNING_64_H_

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

/* Assumed type punning valid in C11. */

/* < https://stackoverflow.com/a/11640603 > */

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------

typedef union {
    double      z_d;
    uint64_t    z_u;
    } t_pun64;

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

static inline void pun64_setDouble (t_pun64* p, double f)
{
    p->z_d = f;
}

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

static inline double pun64_getDouble (t_pun64* p)
{
    return p->z_d;
}

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
#endif // M_PUNNING_64_H_
