void HueSelector::mouseDrag (const juce::MouseEvent& e)
{
    const float y = static_cast<float> (e.y - edge_);
    const float h = static_cast<float> (getHeight() - (edge_ * 2));
    
    const float hue = y / h;
    
    owner_.setHue (hue);
}

