void ParameterText::update()
{
    auto* p = findParentComponentOfClass<juce::PropertyPanel>();
    
    if (p) {
        setPreferredHeight (getPropertyComponentHeight());
        // editor_.update ();
        p->resized();
        resized();
    }
}
    
