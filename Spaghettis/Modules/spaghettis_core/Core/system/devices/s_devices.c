#include "../../m_headers.h"

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

void devices_initialize (t_devices *d)
{
    int i;

    for (i = 0; i < DEVICES_MAXIMUM_IO; i++) {
    //
    d->d_in[i]  = NULL;
    d->d_out[i] = NULL;
    //
    }
}

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

void devices_copy (t_devices *d, t_devices *from)
{
    int i;

    for (i = 0; i < DEVICES_MAXIMUM_IO; i++) {
    //
    d->d_in[i]  = from->d_in[i];
    d->d_out[i] = from->d_out[i];
    //
    }
}

