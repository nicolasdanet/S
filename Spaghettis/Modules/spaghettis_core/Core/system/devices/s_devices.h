// MARK: -

void        devices_initialize                  (t_devices *d);

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

void        devices_copy                        (t_devices *d, t_devices *from);

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

t_error     devices_checkAudio                  (t_devices *d);
int         devices_getTotalOfChannelsIn        (t_devices *d);
