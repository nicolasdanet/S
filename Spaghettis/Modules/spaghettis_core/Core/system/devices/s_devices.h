// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

t_symbol    *devices_getInName                  (t_devices *d, int n);
t_symbol    *devices_getOutName                 (t_devices *d, int n);

int         devices_getInChannels               (t_devices *d, int n);
int         devices_getOutChannels              (t_devices *d, int n);

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
#endif // S_DEVICES_H_
