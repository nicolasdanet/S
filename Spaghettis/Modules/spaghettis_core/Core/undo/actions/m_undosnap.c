static t_class *undosnap_class;         /* Shared. */

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

typedef struct _undosnap {
    t_undoaction    x_undo;             /* Must be the first. */
    } t_undosnap;

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

t_undoaction *undosnap_new (void)
{
    t_undoaction *x = (t_undoaction *)pd_new (undosnap_class);
    
    x->ua_id    = 0;
    x->ua_type  = UNDO_SNAP;
    x->ua_safe  = 1;
    x->ua_label = sym_snap;
    
    return x;
}

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

void undosnap_setup (void)
{
    t_class *c = NULL;
    
    c = class_new (sym_undosnap,
            NULL,
            NULL,
            sizeof (t_undosnap),
            CLASS_INVISIBLE,
            A_NULL);
    
    undosnap_class = c;
}

void undosnap_destroy (void)
{
    class_free (undosnap_class);
}

