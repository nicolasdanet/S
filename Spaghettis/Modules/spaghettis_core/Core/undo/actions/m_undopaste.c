static t_class *undopaste_class;            /* Shared. */

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

typedef struct _undopaste {
    t_undoaction    x_undo;                 /* Must be the first. */
    } t_undopaste;

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

t_undoaction *undopaste_new (void)
{
    t_undoaction *x = (t_undoaction *)pd_new (undopaste_class);
    
    x->ua_id    = 0;
    x->ua_type  = UNDO_PASTE;
    x->ua_safe  = 1;
    x->ua_label = sym_paste;
    
    return x;
}

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

void undopaste_setup (void)
{
    t_class *c = NULL;
    
    c = class_new (sym_undopaste,
            NULL,
            NULL,
            sizeof (t_undopaste),
            CLASS_INVISIBLE,
            A_NULL);
    
    undopaste_class = c;
}

void undopaste_destroy (void)
{
    class_free (undopaste_class);
}

