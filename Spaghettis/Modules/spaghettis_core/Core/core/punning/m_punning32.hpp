#ifndef M_PUNNING_32_HPP_
#define M_PUNNING_32_HPP_

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

typedef struct { uint32_t z_i; } t_pun32;

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

static inline void pun32_setFloat (t_pun32* p, float f)
{
    p->z_i = raw_cast<uint32_t> (f);
}

static inline void pun32_setInteger (t_pun32* p, uint32_t i)
{
    p->z_i = i;
}

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

static inline float pun32_getFloat (t_pun32* p)
{
    return raw_cast<float> (p->z_i);
}

static inline uint32_t pun32_getInteger (t_pun32* p)
{
    return p->z_i;
}

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
#endif // M_PUNNING_32_HPP_
