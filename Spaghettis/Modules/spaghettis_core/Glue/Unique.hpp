struct _object;

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------

class Unique {

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

public:
    explicit Unique (struct _object *o = nullptr);
    
    ~Unique() = default;

public:
    Unique (const Unique&) = default;
    Unique (Unique&&) = default;
    Unique& operator = (const Unique&) = default;
    Unique& operator = (Unique&&) = default;

public:
    bool isValid() const
    {
        return u_ != 0;
    }
    
    bool isRoot() const
    {
        return path_->empty();
    }

private:
    std::shared_ptr<std::vector<uint64_t>> path_;
    uint64_t u_;
};

