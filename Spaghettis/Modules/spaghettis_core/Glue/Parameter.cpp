Parameter& Parameter::setText (const juce::String& s)
{
    parameter_.setProperty (Ids::text, s, nullptr);
    
    return *this;
}

Parameter& Parameter::setInfo (const juce::String& s)
{
    parameter_.setProperty (Ids::info, s, nullptr);
    
    return *this;
}

