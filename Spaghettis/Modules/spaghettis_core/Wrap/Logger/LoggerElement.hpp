namespace spaghettis {

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

enum class LoggerType { normal, system, warning, error };

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

class LoggerElement {

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

public:
    explicit LoggerElement (const juce::String& text, LoggerType type, const core::UniquePath& path) :
        text_ (text),
        type_ (type),
        path_ (path)
    {
        // TODO: Make it trivially copyable for efficiency?
        
        static_assert (std::is_trivially_copyable_v<LoggerElement> == false);
    }

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

public:
    juce::String getText() const
    {
        return text_;
    }
    
    LoggerType getType() const
    {
        return type_;
    }

    core::UniquePath getUniquePath() const
    {
        return path_;
    }
    
private:
    juce::String text_;
    LoggerType type_;
    core::UniquePath path_;
};

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------

}

