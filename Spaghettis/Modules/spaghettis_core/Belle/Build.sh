#! /usr/bin/env bash

# ------------------------------------------------------------------------------------------------------------
# ------------------------------------------------------------------------------------------------------------

# Must be executed at the same level.

rep=$(pwd); [ "${0%/*}" = "." ] || exit 1

# ------------------------------------------------------------------------------------------------------------
# ------------------------------------------------------------------------------------------------------------

# Build the example.

echo "Build Belle ..."

g++ "-std=c++11" "${rep}/Tools/Belle.cpp" -o "${rep}/Belle" "-I${rep}/Source/" || exit 1

# ------------------------------------------------------------------------------------------------------------
# ------------------------------------------------------------------------------------------------------------

# Launch it.

echo "Start Belle ..."

"./Belle" || exit 1

