struct Functors {

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------

static std::function<void()> repaint (juce::Component* component)
{
    return [c = juce::Component::SafePointer<juce::Component> (component)]()
    {
        if (c.getComponent()) { c->repaint(); } else { jassertfalse; }
    };
}

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------

};

