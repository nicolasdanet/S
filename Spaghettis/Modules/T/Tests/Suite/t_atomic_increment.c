void *test_incrementThread (void *x)
{
    int i, n = ttt_threadGetCurrent ((TTTThreadProperties *)x);
    TTTWaste w;
    
    ttt_wasteInit (&w, n);
    
    ttt_threadWaitOnLatch ((TTTThreadProperties *)x);
    
    if ((n % 2) == 0) {
    //
    for (i = 0; i < TEST_LOOP_BIG; i++) {
        //atomic_int32Increment (&test_increment);
        test_increment++;
        ttt_wasteTime (&w);
    }
    //
    } else {
    //
    for (i = 0; i < TEST_LOOP_BIG; i++) {
        //atomic_int32Decrement (&test_increment);
        test_increment--;
        ttt_wasteTime (&w);
    }
    //
    }
    
    return NULL;
}

