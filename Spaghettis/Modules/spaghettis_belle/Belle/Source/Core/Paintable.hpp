namespace belle {

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------

class Painter;

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

class Paintable {

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

public:
    Paintable()
    {
    }
    
    virtual ~Paintable()
    {
    }

    Paintable (const Paintable&) = default;
    Paintable (Paintable&&) = default;
    Paintable& operator = (const Paintable&) = default;
    Paintable& operator = (Paintable&&) = default;

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------

public:
    virtual bool start (Painter& painter) = 0;                  /* Called first. */
    virtual void end   (Painter& painter) = 0;                  /* Called last. */
    virtual void paint (Painter& painter) = 0;                  /* Use coordinates in points. */

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------

public:
    virtual int    getNumberOfPages (Painter& painter) = 0;     /* Must be set in the start method. */
    virtual Points getRequiredSize (Painter& painter) = 0;      /* Ditto. */
};

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------

} // namespace belle

