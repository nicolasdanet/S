namespace spaghettis::data {

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

template <class T> struct Iterator {

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

public:
    explicit Iterator (const juce::ValueTree::Iterator& iterator) : iterator_ (iterator) { }

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

public:
    Iterator& operator ++() { ++iterator_; return *this; }
    bool operator == (const Iterator& iter) const { return iterator_ == iter.iterator_; }
    bool operator != (const Iterator& iter) const { return iterator_ != iter.iterator_; }

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

public:
    T operator *() const { return T (*iterator_); }

private:
    juce::ValueTree::Iterator iterator_;
};

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------

}

