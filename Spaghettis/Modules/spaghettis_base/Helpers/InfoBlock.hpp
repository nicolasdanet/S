namespace spaghettis {

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

class InfoBlock {

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

public:
    explicit InfoBlock (juce::String s = juce::String()) : text_ (s)
    {
    }
    
    ~InfoBlock() = default;

    InfoBlock (const InfoBlock&) = default;
    InfoBlock (InfoBlock&&) = default;
    InfoBlock& operator = (const InfoBlock&) = default;
    InfoBlock& operator = (InfoBlock&&) = default;

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

public:
    juce::String toString() const
    {
        return text_;
    }
    
// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------
// MARK: -

public:
    void append (const juce::String& s)
    {
        text_ += s;
    }
    
private:
    juce::String text_;
};

// -----------------------------------------------------------------------------------------------------------
// -----------------------------------------------------------------------------------------------------------

}

