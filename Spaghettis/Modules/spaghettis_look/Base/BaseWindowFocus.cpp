bool BaseWindowFocus::tryGrabFocusForComponent (juce::Component *c)
{
    c->grabKeyboardFocus();
    
    if (juce::ModalComponentManager::getInstance()->getNumModalComponents() > 0) { return true; }
    else {
        return c->hasKeyboardFocus (true);
    }
}

